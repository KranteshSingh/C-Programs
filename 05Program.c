// WAP to Explain Qualifiers
#include <stdio.h>
int main()
{
    printf("Size of short data type : %d\n", sizeof(short));
    printf("Size of unsigned short data type : %d\n", sizeof(unsigned short));
    printf("Size of signed short data type : %d\n", sizeof(signed short));

    printf("Size of int data type : %d\n", sizeof(int));
    printf("Size of unsigned int data type : %d\n", sizeof(unsigned int));
    printf("Size of signed int data type : %d\n", sizeof(signed int));

    printf("Size of long data type : %d\n", sizeof(long));
    printf("Size of unsigned long data type : %d\n", sizeof(unsigned long));
    printf("Size of signed long data type : %d\n", sizeof(signed long));

    printf("Size of char data type : %d\n", sizeof(char));
    printf("Size of signed char data type : %d\n", sizeof(signed char));
    printf("Size of unsigned char data type : %d\n", sizeof(unsigned char));

    printf("Size of float data type : %d\n", sizeof(float));
    printf("Size of double data type : %d\n", sizeof(double));
    printf("Size of long double data type : %d\n", sizeof(long double));
}